export const getThumbnail = (title: string) => {
  const hash = getUniqueHashFromString(title);

  const thumbnailIndex = getRandomIndexFromHash(
    hash,
    fallbackThumbnails.length
  );

  return fallbackThumbnails[thumbnailIndex];
};
